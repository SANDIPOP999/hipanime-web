<script>
document.addEventListener('DOMContentLoaded', function() {
    // Desktop user profile dropdown
    const desktopUserProfileBtn = document.getElementById('desktopUserProfileBtn');
    const desktopUserMenu = document.getElementById('desktopUserMenu');

    if (desktopUserProfileBtn && desktopUserMenu) {
        desktopUserProfileBtn.addEventListener('click', function(e) {
            e.stopPropagation();
            desktopUserMenu.classList.toggle('active');
        });

        document.addEventListener('click', function(e) {
            if (!desktopUserProfileBtn.contains(e.target) && !desktopUserMenu.contains(e.target)) {
                desktopUserMenu.classList.remove('active');
            }
        });
    }

    // Auth Modal
    const authModal = document.getElementById('authModal');
    const loginBtnHeader = document.getElementById('loginBtnHeader');
    const closeAuthModal = document.getElementById('closeAuthModal');
    const authModalOverlay = document.querySelector('.auth-modal-overlay');

    const loginFormContainer = document.getElementById('loginFormContainer');
    const signupFormContainer = document.getElementById('signupFormContainer');
    const resetPasswordContainer = document.getElementById('resetPasswordContainer');

    const switchToSignup = document.getElementById('switchToSignup');
    const switchToLogin = document.getElementById('switchToLogin');
    const forgotPasswordLink = document.getElementById('forgotPasswordLink');
    const backToLogin = document.getElementById('backToLogin');

    const loginForm = document.getElementById('loginForm');
    const signupForm = document.getElementById('signupForm');
    const resetForm = document.getElementById('resetForm');

    function showAuthModal() {
        authModal.classList.add('active');
        document.body.style.overflow = 'hidden';
    }

    function closeModal() {
        authModal.classList.remove('active');
        document.body.style.overflow = 'auto';
    }

    function switchForm(containerToShow) {
        loginFormContainer.classList.remove('active');
        signupFormContainer.classList.remove('active');
        resetPasswordContainer.classList.remove('active');
        containerToShow.classList.add('active');
    }

    // Open modal
    if (loginBtnHeader) {
        loginBtnHeader.addEventListener('click', showAuthModal);
    }

    // Close modal
    closeAuthModal.addEventListener('click', closeModal);
    authModalOverlay.addEventListener('click', closeModal);

    // Switch forms
    switchToSignup.addEventListener('click', (e) => {
        e.preventDefault();
        switchForm(signupFormContainer);
    });

    switchToLogin.addEventListener('click', (e) => {
        e.preventDefault();
        switchForm(loginFormContainer);
    });

    forgotPasswordLink.addEventListener('click', (e) => {
        e.preventDefault();
        switchForm(resetPasswordContainer);
    });

    backToLogin.addEventListener('click', (e) => {
        e.preventDefault();
        switchForm(loginFormContainer);
    });

    // Login Form Submit
    loginForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        const loading = loginForm.querySelector('.loading');
        const btnText = loginForm.querySelector('.btn-text');
        const btn = loginForm.querySelector('.btn-auth-submit');
        const message = document.getElementById('loginMessage');

        loading.style.display = 'inline-block';
        btnText.textContent = 'Logging in...';
        btn.disabled = true;

        try {
            const response = await fetch('/auth/login.php', {
                method: 'POST',
                body: new FormData(loginForm)
            });

            const data = await response.json();
            message.classList.remove('alert-danger');
            message.classList.add('alert', 'alert-danger');
            message.textContent = data.message;

            if (data.success) {
                message.classList.remove('alert-danger');
                message.classList.add('alert-success');
                setTimeout(() => {
                    // Update UI to show avatar instead of login button
                    updateHeaderAfterLogin(data.user);
                    closeModal();
                }, 500);
            }
        } catch (error) {
            message.classList.add('alert', 'alert-danger');
            message.textContent = 'An error occurred. Please try again.';
        } finally {
            loading.style.display = 'none';
            btnText.textContent = 'Login';
            btn.disabled = false;
        }
    });

    // Update header after login
    function updateHeaderAfterLogin(user) {
        const loginBtn = document.getElementById('loginBtnHeader');
        const userSlot = document.getElementById('user-slot');
        
        if (loginBtn) {
            loginBtn.remove();
        }

        // Create avatar HTML
        const avatarHTML = `
            <button class="desktop-user-profile-btn" id="desktopUserProfileBtn">
                <i class="fas fa-user-circle"></i>
                <span class="desktop-user-name">${escapeHtml(user.username)}</span>
                <i class="fas fa-chevron-down desktop-user-chevron"></i>
            </button>
            <div class="desktop-user-menu" id="desktopUserMenu">
                <ul class="desktop-menu-list">
                    <li><a href="/profile"><i class="fas fa-user"></i> Profile</a></li>
                    <li><a href="/watchlist"><i class="fas fa-bookmark"></i> Watchlist</a></li>
                    <li><a href="/settings"><i class="fas fa-cog"></i> Settings</a></li>
                    <li><hr></li>
                    <li><a href="/logout"><i class="fas fa-sign-out-alt"></i> Logout</a></li>
                </ul>
            </div>
        `;

        userSlot.innerHTML = avatarHTML;

        // Re-attach dropdown functionality
        const newBtn = document.getElementById('desktopUserProfileBtn');
        const newMenu = document.getElementById('desktopUserMenu');

        if (newBtn && newMenu) {
            newBtn.addEventListener('click', function(e) {
                e.stopPropagation();
                newMenu.classList.toggle('active');
            });

            document.addEventListener('click', function(e) {
                if (!newBtn.contains(e.target) && !newMenu.contains(e.target)) {
                    newMenu.classList.remove('active');
                }
            });
        }
    }

    // Helper to escape HTML
    function escapeHtml(text) {
        const map = {
            '&': '&amp;',
            '<': '&lt;',
            '>': '&gt;',
            '"': '&quot;',
            "'": '&#039;'
        };
        return text.replace(/[&<>"']/g, m => map[m]);
    }

    // Close modal function
    function closeModal() {
        authModal.classList.remove('active');
        document.body.style.overflow = 'auto';
    }

    // Sign Up Form Submit
    signupForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        const loading = signupForm.querySelector('.loading');
        const btnText = signupForm.querySelector('.btn-text');
        const btn = signupForm.querySelector('.btn-auth-submit');
        const message = document.getElementById('signupMessage');

        loading.style.display = 'inline-block';
        btnText.textContent = 'Creating Account...';
        btn.disabled = true;

        try {
            const response = await fetch('/auth/register.php', {
                method: 'POST',
                body: new FormData(signupForm)
            });

            const data = await response.json();
            message.classList.remove('alert-success');
            message.classList.add('alert', 'alert-danger');
            message.textContent = data.message;

            if (data.success) {
                message.classList.remove('alert-danger');
                message.classList.add('alert-success');
                signupForm.reset();
                setTimeout(() => {
                    switchForm(loginFormContainer);
                    message.textContent = '';
                }, 2000);
            }
        } catch (error) {
            message.classList.add('alert', 'alert-danger');
            message.textContent = 'An error occurred. Please try again.';
        } finally {
            loading.style.display = 'none';
            btnText.textContent = 'Create Account';
            btn.disabled = false;
        }
    });

    // Password Reset Form Submit
    resetForm.addEventListener('submit', async (e) => {
        e.preventDefault();
        const loading = resetForm.querySelector('.loading');
        const btnText = resetForm.querySelector('.btn-text');
        const btn = resetForm.querySelector('.btn-auth-submit');
        const message = document.getElementById('resetMessage');

        loading.style.display = 'inline-block';
        btnText.textContent = 'Sending...';
        btn.disabled = true;

        try {
            const response = await fetch('/auth/forgot_password.php', {
                method: 'POST',
                body: new FormData(resetForm)
            });

            const data = await response.json();
            message.classList.remove('alert-danger');
            message.classList.add('alert', 'alert-success');
            message.textContent = data.message;

            if (data.success) {
                setTimeout(() => {
                    switchForm(loginFormContainer);
                    resetForm.reset();
                    message.textContent = '';
                }, 2500);
            }
        } catch (error) {
            message.classList.add('alert', 'alert-danger');
            message.textContent = 'An error occurred. Please try again.';
        } finally {
            loading.style.display = 'none';
            btnText.textContent = 'Send Reset Link';
            btn.disabled = false;
        }
    });
});
